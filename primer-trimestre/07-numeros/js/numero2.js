/* ------------------------------------------------ */

// redondear
// "Math.round" function
console.log(Math.round(2.8)); // "Math.round" redondea un número al entero más cercano

// "Math.floor" function
console.log(Math.floor(2.8)); // "Math.floor" redondea un número hacia abajo

// "Math.ceil" function
console.log(Math.ceil(2.8)); // "Math.ceil" redondea un número hacia arriba

/* -------------------------------- */

// "Math.sqrt" function
console.log(Math.sqrt(4)); // "Math.sqrt" calcula la raíz cuadrada de un número

/* -------------------------------- */

// "Math.abs" function
console.log(Math.abs(-5)); // "Math.abs" calcula el valor absoluto de un número

/* -------------------------------- */

// "Math.pow" function
console.log(Math.pow(2, 4)); // "Math.pow" eleva un número a una potencia

/* -------------------------------- */

// "Math.min" function
console.log(Math.min(56, 7, 8, -1)); // "Math.min" devuelve el menor número

/* -------------------------------- */

// "Math.max" function
console.log(Math.max(56, 7, 8, -1)); // "Math.max" devuelve el mayor número

/* -------------------------------- */

// "Math.random" function
console.log("aleatorio: " + Math.round(Math.random()*10)); // "Math.random" devuelve un número aleatorio entre 0 y 1 - en el ejemplo del 0 al 10